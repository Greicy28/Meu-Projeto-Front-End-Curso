const left=document.querySelector(left);
const right=document.querySelector(right);
const container=document.querySelector(container);

left.addEventListener('mouscenter',()=>container.classList.add(hover=left));

left .addEventListener('mousleave',()=>container.classList.renove(hover=left));
right.addEventListener('mouscenter',()=>container.classList.add(hover=right));
right.addEventListener('mouscenter',()=>container.classList.add(hover=right));